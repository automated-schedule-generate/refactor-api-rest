export function removeConnectivesUtil(text: string, lang: Lang = 'pt'): string {
  const connectives = new Set(CONNECTIVES[lang]);

  return text
    .trim()
    .split(' ')
    .filter((word) => !connectives.has(word.toLowerCase()))
    .join(' ')
    .replace(/'/g, "''")
    .trim();
}
